    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Confirma Devolução
        int codA = Integer.parseInt(jTextField1.getText());
        jTextField1.setText("");
        if(!jTextField2.getText().isEmpty()){
            int codL = Integer.parseInt(jTextField2.getText());
            if(controle.Gerencia.DevolveLivro(codL, codA)){
                jLabel19.setText("Livro Devolvido!");
                jButton4.setEnabled(false);
                jTextField2.setEnabled(false);
            } else {
                jLabel19.setText("Não foi Possível devolver o livro!");
            }
        } else { 
            jLabel19.setText("Informe o código do livro.");
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Botão Consultar
        jLabel19.setText("Aguardando Informação");
        jLabel20.setText("Aguardando Informação");        
        if(!jTextField1.getText().isEmpty()){
            if(controle.Gerencia.VerificaAluno(Integer.parseInt(jTextField1.getText()))){
                jLabel3.setText(controle.Gerencia.buscaNomeAluno(Integer.parseInt(jTextField1.getText())));
                CarregaLivrosAlugados(Integer.parseInt(jTextField1.getText()));
                CarregaLivrosReservados(Integer.parseInt(jTextField1.getText()));
            } else {
                jLabel3.setText("Matrícula não encontrada!");
                jButton1.setEnabled(false);
                jButton2.setEnabled(false);
            }            
        } else {
            jLabel3.setText("Campo de consulta vazio!");
            jButton1.setEnabled(false);
            jButton2.setEnabled(false);            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

