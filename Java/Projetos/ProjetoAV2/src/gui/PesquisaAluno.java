    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //Confirmar Cancelar Reserva
        int id_aluno = Integer.parseInt(jTextField1.getText());
        jTextField1.setText("");
        if(!jTextField3.getText().isEmpty()){
            int id_livro = Integer.parseInt(jTextField3.getText());
            if(controle.Gerencia.VerificaAluno(id_aluno)){
                if(controle.Gerencia.CancelaReserva(id_livro, id_aluno)){
                    jLabel20.setText("Reserva Cancelada!");
                    jButton5.setEnabled(false);
                    jTextField3.setEnabled(false);
                } else {
                    jLabel20.setText("Não foi Possível devolver o livro!");
                }  
            } else {
                jLabel20.setText("Matrícula de Aluno Não Encontrada!");
            }
        } else { 
            jLabel20.setText("Informe a matrícula do aluno.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

