    private void CarregaLivrosAlugados(int id_aluno){ //Aqui adiciona as informações na jTable dos Livros Alugados
        ArrayList Livros = new ArrayList();
        Livros = controle.Gerencia.buscaAlugueis(id_aluno); //No Busca Alugueis retorno um arraylist com os códigos dos livros alugados
        DefaultTableModel modelo = new DefaultTableModel(); //Estou indicando que estou criando um modelo de tabela do zero
        modelo.addColumn("Código Livro"); //Adiciono e informo o que vai ter na coluna 1
        modelo.addColumn("Nome Livro"); //Adiciono e informo o que vai ter na coluna 2
        if(Livros.isEmpty()){ //Verifico se o array que criei está vazio
            modelo.addRow(new String[]{"Sem Livros Alugados", "Sem Livros Alugados"}); //Se tiver informo na primeiro linha o que está entre {}. A , significa que estou escrevendo na outra coluna
        } else { // senão, ele vai percorrer com o um for todo o conteudo dentro do ArrayList
            for(int i = 0; i < Livros.size(); i++){ 
                int Codigo = Livros.get(i).hashCode(); //Pego o que está dentro do arraylist na posição i e armazeno numa variável inteira garantindo que o que estou pegando é um inteiro (Ficou mais fácil trabalhar assim)
                String Nome = controle.Gerencia.buscaTituloLivro(Codigo); //Busco o nome do livro de acordo com o código
                modelo.addRow(new String[]{Livros.get(i).toString(), Nome}); //Adiciono uma linha informando que quero adicionar na primeira coluna, o código do livro (preciso converter para String, pois na coluna só adiciona String) e na segunda coluna o nome do livro
            }
            jButton1.setEnabled(true);
        }
        jTable2.setModel(modelo);//Depois de verificar tudo ele manda o jTable correspondente funcionar da maneira que o defini
    }

    private void CarregaLivrosReservados(int id_aluno){ //Aqui adiciona as informações na jTable dos Livros Reservados (Mesma situação do CarregaLivrosAlugados
        ArrayList Livros = new ArrayList();
        Livros = controle.Gerencia.buscaReservas(id_aluno);
        DefaultTableModel modelo2 = new DefaultTableModel();
        modelo2.addColumn("Código Livro");
        modelo2.addColumn("Nome Livro");
        if(Livros.isEmpty()){
            modelo2.addRow(new String[]{"Sem Livros Reservados", "Sem Livros Reservados"});
        } else {
            for(int i = 0; i < Livros.size(); i++){
                int Codigo = Livros.get(i).hashCode();
                String Nome = controle.Gerencia.buscaTituloLivro(Codigo);
                modelo2.addRow(new String[]{Livros.get(i).toString(), Nome});
            }
            jButton2.setEnabled(true);
        }
        jTable1.setModel(modelo2);       
    }

