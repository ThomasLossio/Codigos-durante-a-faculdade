    public void OrganizaSaldo(){
        ArrayList<Aluno> alunos = new ArrayList<>();
        ArrayList<Empregado> empregados = new ArrayList<>();
        
        double total = 0.0;
        alunos = Controle.RetornaAluno();
        empregados = Controle.RetornaEmpregado();
        
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Item");
        modelo.addColumn("Receitas");
        modelo.addColumn("Despesas");
        modelo.addColumn("Saldo");

        if(alunos.isEmpty()){
            modelo.addRow(new String[]{" ", " ", " ", " "});
        } else {
            for(int i = 0; i < alunos.size(); i++){
                total += alunos.get(i).getMensalidade();
                modelo.addRow(new String[]{"Mensalidade de: " + alunos.get(i).getNome(), String.valueOf(alunos.get(i).getMensalidade()), " " , String.valueOf(total)});
            }
        }


        if(empregados.isEmpty()){
            modelo.addRow(new String[]{" ", " ", " ", " "});
        } else {
            for(int i = 0; i < empregados.size(); i++){
                total -= empregados.get(i).getSalario();
                modelo.addRow(new String[]{"Salário de: " + empregados.get(i).getNome(), " ", String.valueOf(empregados.get(i).getSalario()), String.valueOf(total)});
            }
        }

        jTable1.setModel(modelo);            
            
    }


