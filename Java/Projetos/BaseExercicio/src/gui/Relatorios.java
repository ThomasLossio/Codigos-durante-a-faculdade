    public void OrganizaPagar(){
        ArrayList<Empregado> empregados = new ArrayList<>();
        
        empregados = Controle.RetornaEmpregado();
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("ID");
        modelo.addColumn("Item");
        modelo.addColumn("Despesas");

        if(empregados.isEmpty()){
            modelo.addRow(new String[]{" ", " ", " "});
        } else {
            for(int i = 0; i < empregados.size(); i++){
                modelo.addRow(new String[]{String.valueOf(i), "Salário de: " + empregados.get(i).getNome(), String.valueOf(empregados.get(i).getSalario())});
            }
        }

        jTable1.setModel(modelo);                    
    }    

